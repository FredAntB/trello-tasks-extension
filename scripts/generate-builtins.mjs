import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";

const outputFile = resolve(process.cwd(), "src/lib/generated-config.ts");

function readEnvFileValue(key) {
  const envPath = resolve(process.cwd(), ".env");
  if (!existsSync(envPath)) {
    return "";
  }

  const lines = readFileSync(envPath, "utf8").split(/\r?\n/);
  for (const rawLine of lines) {
    const line = rawLine.trim();
    if (!line || line.startsWith("#")) {
      continue;
    }

    const equalIndex = line.indexOf("=");
    if (equalIndex < 1) {
      continue;
    }

    const currentKey = line.slice(0, equalIndex).trim();
    if (currentKey !== key) {
      continue;
    }

    let value = line.slice(equalIndex + 1).trim();
    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1);
    }
    return value.trim();
  }

  return "";
}

const apiKey = process.env.API_KEY?.trim() || readEnvFileValue("API_KEY") || "";

const escapeTsString = (value) =>
  value.replace(/\\/g, "\\\\").replace(/"/g, '\\"');

const content = `// Auto-generated by scripts/generate-builtins.mjs\n// Do not edit manually.\nexport const BUILTIN_API_KEY = "${escapeTsString(apiKey)}";\n`;

mkdirSync(dirname(outputFile), { recursive: true });
writeFileSync(outputFile, content, "utf8");

if (!apiKey) {
  console.warn("[generate-builtins] API_KEY is empty. Set API_KEY in shell env or .env before packaging.");
} else {
  console.log("[generate-builtins] Injected API_KEY into generated config.");
}
